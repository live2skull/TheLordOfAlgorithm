package kr.live2skull.algorithm;

import java.util.*;
import java.io.*;

// 11718 그대로 출력하기 - 출력
// 2020. 03. 31


public class Main
{
    public static void main(String[] args) throws Exception
    {

        Scanner sc = new Scanner(System.in);

        while (sc.hasNext())
        {
            // sc.hasNext() => EOF 를 입력받기 전까지 실행합니다.
            System.out.println(sc.nextLine());
        }
    }
}
